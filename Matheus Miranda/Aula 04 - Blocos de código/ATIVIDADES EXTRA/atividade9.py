def calcular(num1, num2):

  print(f"{num1} + {num2} = {num1 + num2}")
  print(f"{num1} - {num2} = {num1 - num2}")
  print(f"{num1} * {num2} = {num1 * num2}")
  print(f"{num1} / {num2} = {num1 / num2}")

numero1 = float(input('Insira o número 1 com casas decimais: '))
numero2 = float(input('Insira o número 2 com casas decimais: '))
calcular(numero1, numero2)